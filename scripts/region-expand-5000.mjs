/** 본문(문단만 합친 길이) 최소값 — 공백 포함 */
export const MIN_BODY_CHARS = 5000;

/**
 * @param {string} title
 */
function regionLabelFromTitle(title) {
  const head = title.split("—")[0]?.trim() ?? title;
  return head.replace(/\s*출장마사지\s*$/u, "").trim() || head;
}

/**
 * @param {{ title: string; desc: string; slug: string }} post
 * @returns {{ title: string; paragraphs: string[] }[]}
 */
export function getExpandedSectionsForPost(post) {
  const base = getBaseSections(post.slug);
  const region = regionLabelFromTitle(post.title);
  const desc = post.desc;
  const extra = buildDeepSections(region, desc, post.slug);
  const merged = [...base, ...extra];

  let bodyLen = countBodyChars(merged);
  if (bodyLen < MIN_BODY_CHARS) {
    merged.push(...buildFillerSections(region, MIN_BODY_CHARS - bodyLen, post.slug));
    bodyLen = countBodyChars(merged);
  }
  if (bodyLen < MIN_BODY_CHARS) {
    throw new Error(
      `[region-expand-5000] ${post.slug}: 본문 ${bodyLen}자 — ${MIN_BODY_CHARS}자 미만. 스크립트를 보강하세요.`,
    );
  }
  return merged;
}

function countBodyChars(sections) {
  let n = 0;
  for (const s of sections) {
    for (const p of s.paragraphs) n += p.length;
  }
  return n;
}

/** 슬라이드마다 다른 문장 변주를 위해 간단한 시드 */
function pickVariant(slug, i, mod) {
  let h = 0;
  for (let k = 0; k < slug.length; k++) h = (h + slug.charCodeAt(k) * (i + 1)) % 999983;
  return Math.abs(h) % mod;
}

