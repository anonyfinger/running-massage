/**
 * 드물하게 합계가 부족할 때만 추가 (문단 길이 가변)
 * @param {string} region
 * @param {number} needChars
 * @param {string} slug
 */
function buildFillerSections(region, needChars, slug) {
  const chunks = [];
  let acc = 0;
  let i = 0;
  while (acc < needChars && i < 40) {
    const paragraph = fillerParagraph(region, slug, i);
    chunks.push(paragraph);
    acc += paragraph.length;
    i++;
  }
  if (acc < needChars) {
    throw new Error(`[region-expand-5000] filler 부족: need ${needChars}, got ${acc}`);
  }
  return [
    {
      title: `${region} 안내 보강 — 추가로 읽어두면 좋은 내용`,
      paragraphs: chunks,
    },
  ];
}

